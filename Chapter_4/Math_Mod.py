# useful in many different field, usualy returns numbers as floats so precision is not lost
# ceil - rounds up, floor - rounds down

import math

num1 = math.sqrt(144)
print("num1:",num1)

num2 = math.pow(2, 5)
# means 2 to the 5th power
print("num2:",num2)

num3 = math.ceil(34.68)
print("num3:",num3)

