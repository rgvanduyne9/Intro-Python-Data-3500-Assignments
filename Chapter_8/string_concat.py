# string concat
first_name = "Rachel"
last_name = "VanDuyne"

full_name = first_name + last_name
print(full_name)

full_name = first_name + " " + last_name
print(full_name)

full_name += ", welcome!"
print(full_name)

# string multiply
exclamation = "!"
print(exclamation)

exclamation *= 100
print(exclamation)